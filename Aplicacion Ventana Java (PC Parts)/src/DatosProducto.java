    private void BOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BOkActionPerformed
        // TODO add your handling code here:
        if (TFNombre.getText().length() > 0 && TFOferta.getText().length() > 0 && TFPrecio.getText().length() > 0) {
            if (!(Double.parseDouble(TFOferta.getText()) >= 0 && Double.parseDouble(TFOferta.getText()) < 1)) {
                JOptionPane.showMessageDialog(null, "Oferta debe ser mayor o igual que 0 y menor que 1.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if ((CBTipoProducto.getSelectedItem().equals("CPU") || CBTipoProducto.getSelectedItem().equals("Motherboard")) && TFChipset.getText().length() == 0) {
                    JOptionPane.showMessageDialog(null, "Debes introducir el chipset.", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    POK = true;
                    TipoProducto tipo = TipoProducto.valueOf("" + CBTipoProducto.getSelectedItem());
                    System.out.println(tipo);
                    if (tipo.equals(TipoProducto.CPU) || tipo.equals(TipoProducto.Motherboard)){
                         VAdmin.setDatosProducto(TFNombre.getText(), Double.parseDouble(TFPrecio.getText()), Double.parseDouble(TFOferta.getText()), tipo, TAEspecificaciones.getText(), TFChipset.getText());
                    }
                    else{
                        System.out.print("Vaya me he librado pero sigue sin ir siendo tipo "+tipo);
                        VAdmin.setDatosProducto(TFNombre.getText(), Double.parseDouble(TFPrecio.getText()), Double.parseDouble(TFOferta.getText()), tipo, TAEspecificaciones.getText(),"");
                    }
                    this.dispose();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Algun campo esta vacio.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BOkActionPerformed

    private void TFPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFPrecioKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || c == '.')) {
            evt.consume();
        }
    }//GEN-LAST:event_TFPrecioKeyTyped

