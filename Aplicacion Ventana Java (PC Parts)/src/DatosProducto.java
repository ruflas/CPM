    private void CBTipoProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBTipoProductoActionPerformed
        // TODO add your handling code here:
        if (CBTipoProducto.getSelectedItem().equals("CPU") || CBTipoProducto.getSelectedItem().equals("Motherboard")) {
            LChipset.setEnabled(true);
            TFChipset.setEnabled(true);
        } else {
            LChipset.setEnabled(false);
            TFChipset.setEnabled(false);
        }
    }//GEN-LAST:event_CBTipoProductoActionPerformed

    private void cargaTipoProductos() {
        for (TipoProducto tipo : TipoProducto.values()) {
            CBTipoProducto.addItem(tipo.toString());
        }
    }

